#include <unistd.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
		i++;
	return (i);
}

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int	ft_str_is_numeric(char str)
{
	if (str >= '1' && str <= '4')
		return (1);
	return (0);
}
