int	ft_sqrt(int nb)
{
	int	i;

	if (nb < 0 || nb == 2)
		return (0);
	i = -1;
	while (++i <= nb / 2 && i <= 46341)
		if (i * i == nb)
			return (i);
	return (i);
}
